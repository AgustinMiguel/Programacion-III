
public class Tarea {
 private int duracion;
 private String descripcion;

	public Tarea(int duracion, String descripcion) {
		this.duracion = duracion;
		this.descripcion = descripcion;
	}

	public int getDuracion() {
		return duracion;
	}

	public String getDescripcion() {
		return descripcion;
	}

}
